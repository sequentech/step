import {gql} from "@apollo/client"

export const DELETE_ELECTION_EVENT = gql`
    mutation DeleteElectionEvent($electionEventId: String!) {
        delete_election_event(election_event_id: $electionEventId) {
            id
            error_msg
            task_execution {
                id
                name
                execution_status
                created_at
                start_at
                end_at
                logs
                annotations
                labels
                executed_by_user
                tenant_id
                election_event_id
                type
            }
        }
    }
`
